const mongoose = require("mongoose");

const userSchema = new mongoose.Schema(
  {
    order_number: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    education: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const UserList = mongoose.model("UserList", userSchema);

module.exports = UserList;
